int	ft_sqrt(int nb)
{
	int	i;

	if (nb < 0)
		return (0);
	i = 0;
	while (i * i < nb)
	{
		i += 1;
		if (i > 46340)
			return (0);
	}
	if (i * i == nb)
		return (i);
	else
		return (0);
}